// Helper function to get image by ID
export const getImageById = (id) => {
    return galleryImages.find(image => image.id === parseInt(id));
};

// Helper function to get images by theme
export const getImagesByTheme = (theme) => {
    if (theme === 'all') return galleryImages;
    return galleryImages.filter(image => image.theme === theme);
};

// Helper function to get featured images
export const getFeaturedImages = () => {
    return galleryImages.filter(image => image.featured);
};

// Helper function to search images
export const searchImages = (query) => {
    const searchTerm = query.toLowerCase().trim();
    if (!searchTerm) return galleryImages;
    
    return galleryImages.filter(image => 
        image.title.toLowerCase().includes(searchTerm) ||
        image.description.toLowerCase().includes(searchTerm) ||
        image.tags.some(tag => tag.toLowerCase().includes(searchTerm)) ||
        image.theme.toLowerCase().includes(searchTerm)
    );
};

// Helper function to sort images
export const sortImages = (images, sortBy) => {
    const sorted = [...images];
    
    switch(sortBy) {
        case 'newest':
            return sorted.sort((a, b) => new Date(b.date) - new Date(a.date));
        case 'popular':
            return sorted.sort((a, b) => b.views - a.views);
        case 'random':
            return sorted.sort(() => Math.random() - 0.5);
        default:
            return sorted;
    }
};

// Get unique themes
export const getUniqueThemes = () => {
    const themes = galleryImages.map(image => image.theme);
    return [...new Set(themes)];
};

// Get stats
export const getGalleryStats = () => {
    const totalImages = galleryImages.length;
    const totalThemes = getUniqueThemes().length;
    
    // Calculate total size (mock - in real app this would come from API)
    const totalSize = galleryImages.reduce((sum, image) => {
        const size = parseFloat(image.size) || 0;
        return sum + size;
    }, 0);
    
    // Calculate total views and likes
    const totalViews = galleryImages.reduce((sum, image) => sum + image.views, 0);
    const totalLikes = galleryImages.reduce((sum, image) => sum + image.likes, 0);
    
    // Count images per theme
    const themeCounts = {};
    galleryImages.forEach(image => {
        themeCounts[image.theme] = (themeCounts[image.theme] || 0) + 1;
    });
    
    return {
        totalImages,
        totalThemes,
        totalSize: `${totalSize.toFixed(0)}MB`,
        totalViews: totalViews.toLocaleString(),
        totalLikes: totalLikes.toLocaleString(),
        themeCounts,
        featuredCount: galleryImages.filter(img => img.featured).length,
        averageResolution: '1920×1080',
        mostPopularImage: galleryImages.reduce((max, image) => 
            image.views > max.views ? image : max
        ),
        newestImage: galleryImages.reduce((newest, image) => 
            new Date(image.date) > new Date(newest.date) ? image : newest
        )
    };
};

// Function to add new image (for future use)
export const addImage = (imageData) => {
    const newId = Math.max(...galleryImages.map(img => img.id)) + 1;
    const newImage = {
        id: newId,
        ...imageData,
        views: 0,
        likes: 0,
        date: new Date().toISOString().split('T')[0],
        featured: false
    };
    
    galleryImages.unshift(newImage); // Add to beginning
    return newImage;
};

// Function to update image data
export const updateImage = (id, updates) => {
    const index = galleryImages.findIndex(image => image.id === id);
    if (index !== -1) {
        galleryImages[index] = { ...galleryImages[index], ...updates };
        return galleryImages[index];
    }
    return null;
};

// Function to increment views
export const incrementViews = (id) => {
    const image = getImageById(id);
    if (image) {
        image.views++;
        return image.views;
    }
    return null;
};

// Function to toggle like
export const toggleLike = (id) => {
    const image = getImageById(id);
    if (image) {
        image.likes++;
        return image.likes;
    }
    return null;
};

// Export default for convenience
